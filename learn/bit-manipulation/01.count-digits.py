import math
# ---------------------------------------------------------------------------- #
#                             Approach-1: Division                             #
# ---------------------------------------------------------------------------- #
def countDigitsDiv(n):
    count = 0

    while n:
        n = n // 10
        count += 1
    
    return count


# ---------------------------------------------------------------------------- #
#                            Approach-2: Logarithmic                           #
# ---------------------------------------------------------------------------- #
def countDigitsLog(n):
    if n == 0:
        return -1
    return math.floor(math.log10(n) + 1)

# ---------------------------------------------------------------------------- #
#                             Approach-3: Recursive                            #
# ---------------------------------------------------------------------------- #
def countDigitsRec(n):
    if n == 0:
        return 0
    
    return 1 + countDigitsRec(n // 10)

# ---------------------------------------------------------------------------- #
#                             Converting to string                             #
# ---------------------------------------------------------------------------- #
def countDigitsCvt(n):
    if n < 0:
        return len(str(n)) - 1
    return len(str(n))

# ---------------------------------------------------------------------------- #
#                                   Execution                                  #
# ---------------------------------------------------------------------------- #

print("Number of digits: ", countDigitsCvt(0))