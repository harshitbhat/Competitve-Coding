# ---------------------------------------------------------------------------- #
#                           Approach-1: Bit-shifting                           #
# ---------------------------------------------------------------------------- #


def decimalToBinary(n):
    ans = ""

    while(n):
        dig = n & 1
        ans = str(dig) + ans
        n = n >> 1
    
    return ans

# ---------------------------------------------------------------------------- #
#                           Approach-2 : Using Stacks                          #
# ---------------------------------------------------------------------------- #


def decimalToBinaryStk(n):
    stack = []

    while n:
        rem = n % 2
        stack.append(rem)
        n = n >> 1

    stack.reverse()
    
    return ''.join(map(str, stack))

# ---------------------------------------------------------------------------- #
#                                   Execution                                  #
# ---------------------------------------------------------------------------- #

print("Binary Representation: ", decimalToBinaryStk(125))
